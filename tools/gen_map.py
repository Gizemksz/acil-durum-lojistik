edges = []
for f, t in edges_list:
    fn, tn = nodes_map[f], nodes_map[t]
    dist = haversine(fn["lat"], fn["lng"], tn["lat"], tn["lng"])
    speed = 50 if dist > 800 else (40 if dist > 400 else 30)
    edges.append({"from": f, "to": t, "weight": round(dist), "speed": speed})
    edges.append({"from": t, "to": f, "weight": round(dist), "speed": speed})

